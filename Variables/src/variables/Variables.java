package variables;

public class Variables {

    public static void main(String[] args) {
        // TODO code application logic here
        
        //Variables: es un espacio de memoria que guardar algo
        
        //Tipos y conceptos de variables
        
        //int: son los nuemros enteros positivos o negativos = 1,2,3,-1,-2,-3
        //float: son los numeros decimales (hasta 7 digitos) = 1.2, 3.2, -4.3, -5.6        
        //double: son numeros decimales (hasta 15 digitos) = 1.2, 3.2, -4.3, -5.6
        //String: te guarda texto o cadenas = "Hola Mundo"
        //char: guarda un solo caracter = A, B, C, D
        //boolean: guarda valores booleanos = True or False
        
        
        // Creamos la primera variable tipo entero
        int numero;        
        numero = 5;        
        System.out.println(numero);
        
        
        //Creamos la primera variable de tipo flotante        
        float numero1;
        numero1 = 1.2f; //se agrega la f para quitar el error
        System.out.println(numero1);
        
   
        //Creamos la primera variable de tipo double        
        double numero2;
        numero2 = 1.2;
        System.out.println(numero2);
        
        
        //Creamos una variable tipo String
        String texto = "Hola me llamo Euler";
        texto += " y soy de Valledupar";
        System.out.println(texto);
        
        
        //Creamos una variable char
        char caracter = 'A';
        System.out.println(caracter);
        
        
        //Creamos una variable booleana
        boolean variableVerdadera = false;
        System.out.println(variableVerdadera);
        
      
        
        
        
       
        
        
        
    }
    
}
